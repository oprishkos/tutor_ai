def seed():
    db = SessionLocal()
    existing = db.query(Task).count()
    if existing > 0:
        print(f"База уже содержит {existing} заданий. Для повторного заполнения удали tutor.db")
        db.close()
        return

    for row in TASKS:
        grade, topic, subtopic, level, task_text, answer, hint = row
        obj = Task(
            grade=grade,
            topic=topic,
            subtopic=subtopic,
            level=LevelEnum(level),
            task=task_text,
            answer=answer,
            hint=hint,
            source="seed",
        )
        db.add(obj)

    db.commit()
    db.close()
    print(f"✓ Добавлено {len(TASKS)} заданий в базу данных.")
    print(f"  Классы:  5, 6, 7, 8, 9")
    print(f"  Уровни: weak / medium / strong")


if __name__ == "__main__":
    seed()