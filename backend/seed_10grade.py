GRADE = 10

def seed():
    db = SessionLocal()

    existing = db.query(Task).filter(Task.grade == GRADE).count()
    if existing > 0:
        print(f"В базе уже есть {existing} заданий для {GRADE} класса.")
        answer = input("Добавить ещё? (y/n): ").strip().lower()
        if answer != 'y':
            db.close()
            return

    added = 0
    for topic, subtopic, level, task_text, answer, hint in TASKS:
        exists = db.query(Task).filter(
            Task.grade == GRADE,
            Task.task == task_text
        ).first()
        if exists:
            continue
        obj = Task(
            grade=GRADE,
            topic=topic,
            subtopic=subtopic,
            level=LevelEnum(level),
            task=task_text,
            answer=answer,
            hint=hint,
            source="seed",
        )
        db.add(obj)
        added += 1

    db.commit()
    db.close()

    print(f"\n✓ Добавлено {added} заданий для {GRADE} класса.")
    print(f"  Темы:")

    topics = {}
    for row in TASKS:
        topics[row[0]] = topics.get(row[0], 0) + 1
    for t, n in topics.items():
        print(f"    {t[:55]:<55} {n} зад.")


if __name__ == "__main__":
    seed()